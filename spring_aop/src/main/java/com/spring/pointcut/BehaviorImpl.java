package com.spring.pointcut;

public class BehaviorImpl implements Behavior {

	@Override
	public void 잠자기() {
		System.out.println("쿨쿨 잠이온다냥");

	}

	@Override
	public void 공부하기() {
		System.out.println("공부 열심히 한다냥");

	}

	@Override
	public void 밥먹기() {
		System.out.println("냠냠.. 왕왕...와아앙ㅇ쩝");

	}

	@Override
	public void 데이트() {
		System.out.println("뎃투 하러간다냥");

	}

	@Override
	public void 운동() {
		System.out.println("헛둘 헛둘~~");

	}

	@Override
	public void 놀기() {
		System.out.println("냐냐냐ㅑ냐냐(채터링)");

	}

	@Override
	public void 정신수양() {
		System.out.println("정신집즁~~~~");

	}

}
